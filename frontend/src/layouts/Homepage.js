import React from "react";
import { useLocation, Route, Routes } from "react-router-dom";
import { Container, Row, Col } from "reactstrap";

// core components
import HomepageNavbar from "../components/Navbars/HomepageNavbar";
import HomepageFooter from "../components/Footers/HomepageFooter";

import routes from "../routes";

const Homepage = (props) => {

  const mainContent = React.useRef(null);
  const location = useLocation();

  React.useEffect(() => {
    document.body.classList.add("bg-default");
    return () => {
      document.body.classList.remove("bg-default");
    };
  }, []);

  React.useEffect(() => {
    document.documentElement.scrollTop = 0;
    document.scrollingElement.scrollTop = 0;
    mainContent.current.scrollTop = 0;
  }, [location]);

  const getRoutes = (routes) => {
    return routes.map((prop, key) => {
      if (prop.layout === "/home") {
        return (
          <Route
            path={prop.layout + prop.path}
            component={prop.component}
            key={key}
          />
        );
      } else {
        return null;
      }
    });
  };

  return (
    <>
      <div className="main-content" ref={mainContent}>
        <HomepageNavbar />
        <div className="header bg_gradient_personalized py-7 py-lg-8">

          <Container>
            <div className="header-body text-center mb-7">
              <Row className="justify-content-center">
                <Col lg="5" md="6">
                  <h1 className="text-white">Welcome!</h1>
                  <p className="text-lead text-light">
                    Here is the homepage of our team project
                  </p>
                </Col>
              </Row>
            </div>
          </Container>

        </div>
        
        {/* Page content */}
        <Container className="mt--8 pb-5">
          <Row className="justify-content-center">
            <Routes>
              {getRoutes(routes)}
              {/* <Navigate from="*" to="/auth/login" /> */}
            </Routes>
          </Row>
        </Container>
      </div>
      <HomepageFooter />
    </>
  );
};

export default Homepage;
